public class Operators {
    public static void main(String[] args) {
        int a = 10, b = 3;
        
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);
        
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);
        
        System.out.println(true && false);
        System.out.println(true || false);
        System.out.println(!true);
        
        System.out.println(a << 1);
        System.out.println(a >> 1);
        System.out.println(~a);
        System.out.println(a & b);
        System.out.println(a | b);
        System.out.println(a ^ b);
    }
}
